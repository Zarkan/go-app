// +build ignore

package main

import (
	"fmt"
	"io/ioutil"
	"os"
	"path/filepath"
	"runtime"
)

func main() {
	f, err := os.Create("templates.go")
	if err != nil {
		panic(err)
	}
	defer f.Close()

	fmt.Fprintln(f, "package main")
	fmt.Fprintln(f)
	fmt.Fprintln(f, "// Code generated by go generate; DO NOT EDIT.")

	gen := []struct {
		Var      string
		Filename string
	}{
		{Var: "goappJS", Filename: "goapp.js"},
		{Var: "wasmExecJS", Filename: filepath.Join(
			runtime.GOROOT(),
			"misc",
			"wasm",
			"wasm_exec.js",
		)},
		{Var: "mainServer", Filename: "main-server.go.tmpl"},
		{Var: "mainWasm", Filename: "main-wasm.go.tmpl"},
	}

	for _, g := range gen {
		b, err := ioutil.ReadFile(g.Filename)
		if err != nil {
			panic(err)
		}

		fmt.Fprintln(f)
		fmt.Fprintf(f, "const %s = %q", g.Var, b)
	}
}
